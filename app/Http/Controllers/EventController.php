    {
        $eventData = $request->validated();
        $event = Event::create($eventData);

        $event->days()->createMany(array_map(function($day) {
            return compact('day');
        },$eventData['eventDays']));


        return response()
            ->json([
                'status' => 'success',
                'data' => ['event' => $event->load('days:event_id,day')]
            ],Response::HTTP_CREATED);

    }

